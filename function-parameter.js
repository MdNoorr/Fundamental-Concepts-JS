// Single Parameter

function myName(name) {
  console.log("My name is: " + name);
}
myName("Nur Muhammad");

function myCountry(country) {
  console.log("My country name is: " + country);
}
myCountry("Bangladesh");


// Multiple Parameter
function add(x, y) {
  console.log(x + y);
}

add(11, 22);

function sub(x, y) {
  console.log(x - y);
}
sub(11, 7);

function mul(x, y) {
  console.log(x * y);
}
mul(11, 22);

function div(x, y) {
  console.log(x / y);
}

div(11, 7);

function mod(x, y) {
  console.log(x % y);
}
mod(11, 7);


function addition(x, y) {
  console.log(x + y)
}

addition(1, 2)
addition(22, 2)
addition(4, 2)
addition(7, 2)